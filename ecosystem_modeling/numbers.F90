! program to explain the numbers and dta types in fortran
program numbers

    implicit none ! do not assume the below
    !l, m, n ... integers
    !a,b,c,x,y,z ... Real/Floating

    integer a, b, c
    real ar, br, cr 
    double precision ad, bd, cd ! real but the twice memory
    complex ac, bc, cc
    !(4+5i, 7-9.3j)
    character ach, bch, cch
    logical al, bl, cl 

    a = 8
    b = 10


end program numbers